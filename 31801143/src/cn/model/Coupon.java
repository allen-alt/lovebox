package cn.model;

public class Coupon {
	private int coupon_id;
	private String coupon_content;
	private String coupon_manzu;
	private String coupon_jinqu;
	private java.util.Date coupon_startday;
	private java.util.Date coupon_endday;
	public int getCoupon_id() {
		return coupon_id;
	}
	public void setCoupon_id(int coupon_id) {
		this.coupon_id = coupon_id;
	}
	public String getCoupon_content() {
		return coupon_content;
	}
	public void setCoupon_content(String coupon_content) {
		this.coupon_content = coupon_content;
	}
	public String getCoupon_manzu() {
		return coupon_manzu;
	}
	public void setCoupon_manzu(String coupon_manzu) {
		this.coupon_manzu = coupon_manzu;
	}
	public String getCoupon_jinqu() {
		return coupon_jinqu;
	}
	public void setCoupon_jinqu(String coupon_jinqu) {
		this.coupon_jinqu = coupon_jinqu;
	}
	public java.util.Date getCoupon_startday() {
		return coupon_startday;
	}
	public void setCoupon_startday(java.util.Date coupon_startday) {
		this.coupon_startday = coupon_startday;
	}
	public java.util.Date getCoupon_endday() {
		return coupon_endday;
	}
	public void setCoupon_endday(java.util.Date coupon_endday) {
		this.coupon_endday = coupon_endday;
	}
	
	

}
