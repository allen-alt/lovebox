package cn.model;

public class Menu {
	private int menu_id;
	private String menu_name;
	private String menue_materials;
	private String menu_step;
	private String menu_pic;
	public int getMenu_id() {
		return menu_id;
	}
	public void setMenu_id(int menu_id) {
		this.menu_id = menu_id;
	}
	public String getMenu_name() {
		return menu_name;
	}
	public void setMenu_name(String menu_name) {
		this.menu_name = menu_name;
	}
	public String getMenue_materials() {
		return menue_materials;
	}
	public void setMenue_materials(String menue_materials) {
		this.menue_materials = menue_materials;
	}
	public String getMenu_step() {
		return menu_step;
	}
	public void setMenu_step(String menu_step) {
		this.menu_step = menu_step;
	}
	public String getMenu_pic() {
		return menu_pic;
	}
	public void setMenu_pic(String menu_pic) {
		this.menu_pic = menu_pic;
	}
	
	

}
