package cn.model;

public class Goods {
	private int good_id;
	private String goods_name;
	private String goods_price;
	private String vip_price;
	private String goods_amount;
	private String goods_guige;
	private String goods_details;
	public int getGood_id() {
		return good_id;
	}
	public void setGood_id(int good_id) {
		this.good_id = good_id;
	}
	public String getGoods_name() {
		return goods_name;
	}
	public void setGoods_name(String goods_name) {
		this.goods_name = goods_name;
	}
	public String getGoods_price() {
		return goods_price;
	}
	public void setGoods_price(String goods_price) {
		this.goods_price = goods_price;
	}
	public String getVip_price() {
		return vip_price;
	}
	public void setVip_price(String vip_price) {
		this.vip_price = vip_price;
	}
	public String getGoods_amount() {
		return goods_amount;
	}
	public void setGoods_amount(String goods_amount) {
		this.goods_amount = goods_amount;
	}
	public String getGoods_guige() {
		return goods_guige;
	}
	public void setGoods_guige(String goods_guige) {
		this.goods_guige = goods_guige;
	}
	public String getGoods_details() {
		return goods_details;
	}
	public void setGoods_details(String goods_details) {
		this.goods_details = goods_details;
	}
	

}
